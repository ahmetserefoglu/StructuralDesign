package bridgeNewDesign;

public class Muzik {

    private String adi;
    private String ses;

    public Muzik(String adi, String ses){
        this.adi = adi;
        this.ses = ses;
    }


    public String getAdi() {
        return this.adi;
    }

    public void setAdi(String adi) {
        this.adi = adi;
    }

    public String getSes() {
        return this.ses;
    }

    public void setSes(String ses) {
        this.ses = ses;
    }

}